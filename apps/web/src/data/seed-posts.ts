export function getPostsByCategory(
  category: SeedPost["category"],
): SeedPost[] {
  return seedPosts.filter((p) => p.category === category);
}

export function getPostBySlug(slug: string): SeedPost | undefined {
  return seedPosts.find((p) => p.slug === slug);
}

export function getRecentPosts(limit = 6): SeedPost[] {
  return [...seedPosts]
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
    .slice(0, limit);
}
